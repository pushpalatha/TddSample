package com.real.sample.calculator.model;

/**
 * The calculator that will perform all the calculations.
 * @author Rainer
 */
public class Calculator {
	
	/**
	 * Multiplies 2 operands with each other.
	 * @param o1	the first operand
	 * @param o2	the 2nd operand
	 * @return the product of the 2 operands
	 */
	public float multiply(float o1, float o2) {
		return o1 * o2;
	}
}
